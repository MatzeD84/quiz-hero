const fs = require('fs');
const path = require('path');

const root = path.resolve(__dirname, '..');

const readJson = relativePath => {
    const raw = fs.readFileSync(path.join(root, relativePath), 'utf8').replace(/^\uFEFF/, '');
    return JSON.parse(raw);
};

const sqlString = value => {
    if (value === null || value === undefined || value === '') {
        return 'NULL';
    }
    return `'${String(value).replace(/\\/g, '\\\\').replace(/'/g, "''")}'`;
};

const sqlJson = value => sqlString(JSON.stringify(value ?? null));
const sqlNumber = value => Number.isFinite(Number(value)) ? String(Number(value)) : '0';
const rows = [];

const categories = readJson('categories.json').categories ?? [];
const tags = readJson('tags.json').tags ?? [];
const feedback = readJson('feedback.json');

rows.push('-- Generated by scripts/build-seed-sql.js');
rows.push('-- Import after database/schema.sql. Creates initial categories, questions, tags and feedback.');
rows.push('-- Does not delete quiz_users or quiz_results.');
rows.push('SET NAMES utf8mb4;');
rows.push('SET FOREIGN_KEY_CHECKS = 0;');
rows.push('START TRANSACTION;');
rows.push('DELETE FROM quiz_questions;');
rows.push('DELETE FROM quiz_categories;');
rows.push('DELETE FROM quiz_tags;');
rows.push('DELETE FROM quiz_feedback;');
rows.push('SET FOREIGN_KEY_CHECKS = 1;');

for (const [index, category] of categories.entries()) {
    rows.push([
        'INSERT INTO quiz_categories',
        '(id, title, description, seo_description, icon, enabled, badge_json, sort_order)',
        'VALUES',
        `(${[
            sqlString(category.id),
            sqlString(category.title),
            sqlString(category.description ?? ''),
            sqlString(category.seoDescription ?? ''),
            sqlString(category.icon ?? null),
            category.enabled === false ? '0' : '1',
            sqlJson(category.badge ?? { active: false, text: '' }),
            sqlNumber((index + 1) * 10),
        ].join(', ')});`,
    ].join(' '));

    const file = category.questionsFile;
    if (!file || !fs.existsSync(path.join(root, file))) {
        continue;
    }
    const questions = readJson(file).questions ?? [];
    for (const [questionIndex, question] of questions.entries()) {
        rows.push([
            'INSERT INTO quiz_questions',
            '(category_id, question, answers_json, correct_index, difficulty, question_type, image_url, tags_json, background_knowledge, active, sort_order)',
            'VALUES',
            `(${[
                sqlString(category.id),
                sqlString(question.question),
                sqlJson(question.answers ?? []),
                sqlNumber(question.correct ?? 0),
                sqlString(question.difficulty ?? 'easy'),
                sqlString(question.type ?? (question.imageUrl ? 'image' : 'text')),
                sqlString(question.imageUrl ?? null),
                sqlJson(question.tag ?? []),
                sqlString(question.backgroundKnowledge ?? null),
                '1',
                sqlNumber((questionIndex + 1) * 10),
            ].join(', ')});`,
        ].join(' '));
    }
}

for (const [index, tag] of tags.entries()) {
    rows.push([
        'INSERT INTO quiz_tags',
        '(id, title, description, icon, badge_json, sort_order)',
        'VALUES',
        `(${[
            sqlString(tag.id),
            sqlString(tag.title),
            sqlString(tag.description ?? ''),
            sqlString(tag.icon ?? ''),
            sqlJson(tag.badge ?? { active: false, text: '' }),
            sqlNumber((index + 1) * 10),
        ].join(', ')});`,
    ].join(' '));
}

for (const [key, messages] of Object.entries(feedback)) {
    rows.push([
        'INSERT INTO quiz_feedback',
        '(feedback_key, messages_json)',
        'VALUES',
        `(${sqlString(key)}, ${sqlJson(messages)});`,
    ].join(' '));
}

rows.push('COMMIT;');

const outputPath = path.join(root, 'database', 'seed.sql');
fs.writeFileSync(outputPath, `${rows.join('\n')}\n`, 'utf8');
console.log(`Wrote ${path.relative(root, outputPath)} with ${categories.length} categories, ${tags.length} tags.`);
